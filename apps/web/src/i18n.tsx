const dictionaries: Record<UiLang, Dict> = { zh, en, ja, ko, ar, ru };

interface I18nContextValue {
  lang: UiLang;
  setLang: (lang: UiLang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

const STORAGE_KEY = "hcs_ui_lang";

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<UiLang>(() => {
    const stored = typeof localStorage !== "undefined" ? localStorage.getItem(STORAGE_KEY) : null;
    return (stored as UiLang) in dictionaries ? (stored as UiLang) : "zh";
  });

  useEffect(() => {
    const meta = UI_LANGUAGES.find((item) => item.code === lang);
    document.documentElement.lang = lang === "zh" ? "zh-Hans" : lang;
    document.documentElement.dir = meta?.rtl ? "rtl" : "ltr";
    try {
      localStorage.setItem(STORAGE_KEY, lang);
    } catch {
      // ignore storage failures (e.g. private mode)
    }
  }, [lang]);

  const setLang = (next: UiLang) => setLangState(next);

  const t = (key: string, vars?: Record<string, string | number>) => {
    const template = dictionaries[lang][key] ?? dictionaries.zh[key] ?? key;
    if (!vars) return template;
    return template.replace(/\{(\w+)\}/g, (match, name: string) =>
      name in vars ? String(vars[name]) : match
    );
  };

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    // Safe fallback so components never crash outside the provider.
    return {
      lang: "zh",
      setLang: () => undefined,
      t: (key: string, vars?: Record<string, string | number>) => {
        const template = dictionaries.zh[key] ?? key;
        return vars ? template.replace(/\{(\w+)\}/g, (m, n: string) => (n in vars ? String(vars[n]) : m)) : template;
      }
    };
  }
  return ctx;
}
